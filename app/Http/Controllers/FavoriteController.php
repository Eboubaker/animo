     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Wallpaper $wallpaper)
    {
        $user = Auth::user();
        if($wallpaper->isFavorite)
        {
            $user->favorites()->where('wallpaper_id', $wallpaper->getKey())->detach();
        }else{
            $user->favorites()->attach($wallpaper);
        }
        return back();
    }
